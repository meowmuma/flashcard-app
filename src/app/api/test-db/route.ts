import { NextResponse } from 'next/server';
import pool from '../../lib/db';

export async function GET() {
  try {
    console.log('üß™ Testing database connection...');
    
    // ‡∏•‡∏≠‡∏á‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡πÅ‡∏•‡∏∞ query ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏á‡πà‡∏≤‡∏¢‡πÜ
    const client = await pool.connect();
    console.log('‚úÖ Database connection successful');
    
    // ‡∏ó‡∏î‡∏™‡∏≠‡∏ö query ‡πÄ‡∏ß‡∏•‡∏≤‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô
    const timeResult = await client.query('SELECT NOW() as current_time');
    console.log('‚úÖ Query successful, current time:', timeResult.rows[0].current_time);
    
    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏ï‡∏≤‡∏£‡∏≤‡∏á users ‡∏°‡∏µ‡∏≠‡∏¢‡∏π‡πà‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
    const tableCheckResult = await client.query(`
      SELECT EXISTS (
        SELECT FROM information_schema.tables 
        WHERE table_name = 'users'
      );
    `);
    
    const tableExists = tableCheckResult.rows[0].exists;
    console.log('üìä Users table exists:', tableExists);
    
    // ‡∏ñ‡πâ‡∏≤‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏°‡∏µ‡∏≠‡∏¢‡∏π‡πà ‡∏•‡∏≠‡∏á‡∏ô‡∏±‡∏ö‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ
    let userCount = 0;
    if (tableExists) {
      const countResult = await client.query('SELECT COUNT(*) FROM users');
      userCount = parseInt(countResult.rows[0].count);
      console.log('üë• Number of users:', userCount);
    }
    
    client.release();
    console.log('üîå Connection released');
    
    return NextResponse.json({
      success: true,
      message: 'Database connection successful',
      currentTime: timeResult.rows[0].current_time,
      usersTableExists: tableExists,
      userCount: userCount,
    });
    
  } catch (error: any) {
    console.error('‚ùå Database test failed:', error);
    
    return NextResponse.json({
      success: false,
      error: error.message,
      code: error.code,
      hint: getErrorHint(error.code, error.message),
    }, { status: 500 });
  }
}

